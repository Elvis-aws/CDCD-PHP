Code Guru
*********
    **************
    Mazon CodeGuru 
    **************
        - It is a developer tool that provides intelligent recommendations to improve code quality and identify an 
          applications most expensive lines of code. 
        - Integrate CodeGuru into your existing software development workflow to automate code reviews during 
          application development and continuously monitor applications performance in production and provide 
          recommendations and visual clues on how to improve code quality.
    *****************
    CodeGuru Reviewer
    *****************
        - This uses machine learning and automated reasoning to identify critical issues, security vulnerabilities, 
          and hard-to-find bugs during application development and provides recommendations to improve code quality.
    *****************
    CodeGuru Profiler
    *****************
        - This helps developers find an applications most expensive lines of code by helping them understand the 
          runtime behavior of their applications, identify and remove code inefficiencies, improve performance, and 
          significantly decrease compute costs. 